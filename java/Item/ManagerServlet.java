        @Override
    protected void doPut(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try (PrintWriter out = response.getWriter()) {
            String pathInfo = request.getPathInfo();
            String[] order = pathInfo.substring(1).split("/");
            String id = order[0];
            String status = order[1];
            String itemID = order[2];
            String quantity = order[3];
            boolean success = dbAccessor.mUpdateOrder(Integer.parseInt(id), status, Integer.parseInt(itemID), Integer.parseInt(quantity));
            out.println(success == true ? 1 : 0);
        }
    }
    
